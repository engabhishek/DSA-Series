# include <iostream>
using namespace std;

int main() {
    int a, b;

    cout << "enter the first value : ";
    cin >> a;

    cout << "enter the second value : ";
    cin >> b;

    cout << "addition of first & second value is  : " << a + b << endl;
    cout << "subtraction of first & second value is  : " << a - b << endl;
    cout << "multiplecation of first & second value is  : " << a * b << endl;
    cout << "division of first to second value is  : " << a / b << endl;
}